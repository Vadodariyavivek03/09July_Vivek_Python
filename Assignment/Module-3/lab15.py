# Write a Python program to create a class and access the properties of the class using an object.

class Book:

    title = "To Kill a Mockingbird"
    author = "Harper Lee"
    page = 281
    language = "English"

b1 = Book()

print("\nTitle ::",b1.title)
print("Author ::",b1.author)
print("Page ::",b1.page)
print("Language ::",b1.language)
print()