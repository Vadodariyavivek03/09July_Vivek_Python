# Write a Python program to sort a list using both sort() and sorted().

my_list = ['Mango', 'Watermelon', 'Apple', 'Pineapple', 'Grapes', 'Banana', 'Orange', 'Cherry', 'Peach']

print(my_list)

my_list.sort()
print("Sorting list : ",my_list)

