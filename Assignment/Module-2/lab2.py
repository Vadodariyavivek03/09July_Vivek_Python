# Write a Python program to access elements at different index positions.

my_list = ['English', 'Math', 21, 89, 'Science', 3.14, True, ['History', 42], 15.36]

print(my_list)

print(my_list[0])  

print(my_list[5])

print(my_list[7])

print(my_list[7][0]) # Accessing the first element of the nested list

print(my_list[2:])

print(my_list[1:6])

print(my_list[:5])

print(my_list[1:8:2])




