# Write a Python program to add elements to a list using insert() and append().

my_list = ['English', 'Math', 21, 89, 'Science', 3.14, True, ['History', 42], 15.36]

print(my_list)

my_list.insert(3, 'Physics') # Insert 'Physics' at index 3.
print(my_list)

my_list.append('Geography')   # Append 'Geography' to the end of the list.
print(my_list)     

